    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        try {
            Statement datosclientes = cn.createStatement();
            ResultSet result = datosclientes.executeQuery("SELECT * FROM clientes");
            while (result.next()) {
                String usuario = result.getString("Correo");
                String usu = lblCorreo.getText();
                String contra = result.getString("Contra");
                String con = pfContra.getText();
                if (usu.equals(usuario) && con.equals(contra)) {
                    Factura ver = new Factura();
                    ver.setVisible(true);
                    dispose();

                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e + "Error en la consulta");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

